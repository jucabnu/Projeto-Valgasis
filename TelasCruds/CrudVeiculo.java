    private void botaoGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGravarActionPerformed

        Veiculo v1 = new Veiculo();

        try {
            if (campoRenavan.getText().equals("")) {
                campoStatus.setText("");
            } else {
                v1 = Banco.selectVeiculo(campoRenavan.getText());
            }

            if (v1.getPlaca().equals("")) {
                // GRAVAR : INSERT
                Veiculo v = new Veiculo();
                v.setRenavan(campoRenavan.getText());
                v.setPlaca(campoPlaca.getText());
                v.setAno(Integer.parseInt(campoAno.getText()));
                v.setCor(campoCor.getText());
                v.setModelo(Integer.parseInt(campoModelo.getText()));
                v.setClienteIn(campoCpf.getText());
                v.setValIn(Double.parseDouble(campoValor.getText()));
                v.setObs_veic(campoObs.getText());
                v.setStatus(false);
                Banco.insertVeiculo(v);
                JOptionPane.showMessageDialog(null, "Veículo cadastrado com sucesso!");
            } else {
                // ALTERAR : UPDATE
                Veiculo v = new Veiculo();
                v.setRenavan(campoRenavan.getText());
                v.setPlaca(campoPlaca.getText());
                v.setAno(Integer.parseInt(campoAno.getText()));
                v.setCor(campoCor.getText());
                v.setModelo(Integer.parseInt(campoModelo.getText()));
                v.setClienteIn(campoCpf.getText());
                v.setValIn(Double.parseDouble(campoValor.getText()));
                v.setObs_veic(campoObs.getText());
                Banco.updateVeiculo(v);
                JOptionPane.showMessageDialog(null, "Alterações gravadas com sucesso!");
            }
        } catch (NumberFormatException | HeadlessException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Formato inválido informado em algum dos campos ou"
                    + "\n" + "campo obrigatório deixado em branco."
                    + "\n" + "Favor reiniciar o cadastro."
                    + "\n" + "(Error Exception: " + e.getMessage());
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_botaoGravarActionPerformed


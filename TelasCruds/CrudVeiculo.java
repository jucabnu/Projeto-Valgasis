    private void campoModeloFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoModeloFocusLost
        // TODO add your handling code here:

        try {
            if (campoModelo.getText().equals("")) {
            } else {
                Modelo model = Banco.selectModelo(Integer.parseInt(campoModelo.getText()));
                jLabelModelo.setText(model.getDes_modelo());
                Marca marc = Banco.selectMarca(model.getMarca());
                campoMarca.setText(Integer.toString(marc.getCod_marca()));
                jLabelMarca.setText(marc.getDes_marca());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Informe apenas números inteiros para o campo código!"
                    + "\n" + "\n" + "(Error Exception: " + e.getMessage());
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_campoModeloFocusLost


    private void campoRenavanFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoRenavanFocusLost

        // MOSTRAR : SELECT

        try {
            if (campoRenavan.getText().equals("")) {
                // nada
                System.out.println("nada1");
            } else {
                Veiculo veic = Banco.selectVeiculo(campoRenavan.getText());
                if (veic.getPlaca().equals("")) {
                    System.out.println("RENAVAN não encontrado...");
                } else {
                    // seleciona modelo, seta o campo e o label
                    Modelo model = Banco.selectModelo(veic.getModelo());
                    campoModelo.setText(Integer.toString(veic.getModelo()));
                    jLabelModelo.setText(model.getDes_modelo());

                    Marca marc = Banco.selectMarca(model.getMarca());
                    campoMarca.setText(Integer.toString(marc.getCod_marca()));
                    jLabelMarca.setText(marc.getDes_marca());

                    Cliente clie = Banco.selectCliente(veic.getClienteIn());
                    campoCpf.setText(veic.getClienteIn());
                    jLabelCpf.setText(clie.getNome());
                }
                campoPlaca.setText(veic.getPlaca());
                campoAno.setText(Integer.toString(veic.getAno()));
                campoCor.setText(veic.getCor());
                campoValor.setText(Double.toString(veic.getValIn()));
                campoObs.setText(veic.getObs_veic());
                if (veic.isStatus() == true) {
                    campoStatus.setText("Vendido!");
                } else {
                    campoStatus.setText("Disponível para venda.");
                }
            }
        } catch (NumberFormatException | HeadlessException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Formato inválido informado em algum dos campos ou"
                    + "\n" + "campo obrigatório deixado em branco."
                    + "\n" + "Favor reiniciar o cadastro."
                    + "\n" + "(Error Exception: " + e.getMessage());
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_campoRenavanFocusLost


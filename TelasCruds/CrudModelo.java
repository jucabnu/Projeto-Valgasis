    private void botaoGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGravarActionPerformed

        Modelo m = new Modelo();
        // m.setCod_modelo(Integer.parseInt(campoCod.getText()));

        try {
            if (campoCod.getText().equals("")) {
                // GRAVAR / INSERT
                m.setDes_modelo(campoDesc.getText());
                m.setMarca(Integer.parseInt(campoCodMarca.getText()));
                Banco.insertModelo(m);
                JOptionPane.showMessageDialog(null, "Modelo foi cadastrado com sucesso!");
            } else {
                // ALTERAR / UPDATE
                m.setCod_modelo(Integer.parseInt(campoCod.getText()));
                m.setDes_modelo(campoDesc.getText());
                m.setMarca(Integer.parseInt(campoCodMarca.getText()));
                Banco.updateModelo(m);
                JOptionPane.showMessageDialog(null, "As alterações foram gravadas com sucesso!");
            }
        } catch (NumberFormatException | HeadlessException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Formato inválido informado em algum dos campos ou"
                    + "\n" + "campo obrigatório deixado em branco."
                    + "\n" + "Favor reiniciar o cadastro."
                    + "\n" + "(Error Exception: " + e.getMessage() + ")");
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_botaoGravarActionPerformed


    private void campoCodMarcaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoCodMarcaFocusLost

        try {
            if (campoCodMarca.getText().equals("")) {
            } else {
                Marca marc = Banco.selectMarca(Integer.parseInt(campoCodMarca.getText()));
                jLabelMarca.setText(marc.getDes_marca());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Informe apenas números inteiros para o campo de código!"
                    + "\n" + ""
                    + "\n" + "(Error Exception: " + e.getMessage() + ")");
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_campoCodMarcaFocusLost

    private void campoCodFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoCodFocusLost

        // MOSTRAR : SELECT

        try {
            if (campoCod.getText().equals("")) {
            } else {
                Modelo model = Banco.selectModelo(Integer.parseInt(campoCod.getText()));
                Marca marc = Banco.selectMarca(model.getMarca());
                campoDesc.setText(model.getDes_modelo());
                campoCodMarca.setText(Integer.toString(model.getMarca()));
                jLabelMarca.setText(marc.getDes_marca());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,
                    "Informe apenas números inteiros para o campo de código!"
                    + "\n" + ""
                    + "\n" + "(Error Exception: " + e.getMessage() + ")");
            botaoLimparActionPerformed(null);
        }
    }//GEN-LAST:event_campoCodFocusLost

